def get_chord(name: str) -> dict | None:
    """Return chord data by name, trying common enharmonic aliases."""
    aliases = {
        "Gb": "F#m", "Db": "C#m", "Ab": "G#m", "Eb": "D#m",
        "A#m": "Bbm", "D#m": "Ebm", "G#m": "Abm",
    }
    return CHORDS.get(name) or CHORDS.get(aliases.get(name, ""))


def list_chords_by_type(chord_type: str) -> list[dict]:
    return [c for c in CHORDS.values() if c.get("type") == chord_type]


def list_chords_by_difficulty(difficulty: str) -> list[dict]:
    return [c for c in CHORDS.values() if c.get("difficulty") == difficulty]
