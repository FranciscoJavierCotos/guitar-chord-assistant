def get_progressions_by_genre(genre: str) -> list[dict]:
    genre = genre.lower().strip()
    return [p for p in PROGRESSIONS if genre in p["genre"]]


def get_progressions_by_key(key: str) -> list[dict]:
    return [p for p in PROGRESSIONS if p["key"].upper() == key.upper()]


def get_progression_by_id(pid: str) -> dict | None:
    return next((p for p in PROGRESSIONS if p["id"] == pid), None)


# Mood → tags mapping for fuzzy mood search
MOOD_TAG_MAP: dict[str, list[str]] = {
    "happy": ["uplifting", "bright", "bouncy", "joyful", "energetic"],
    "sad": ["melancholic", "minor", "introspective", "dark", "emotional"],
    "energetic": ["driving", "fast", "energetic", "anthemic", "aggressive"],
    "melancholic": ["melancholic", "introspective", "minor", "ballad"],
    "romantic": ["warm", "smooth", "gentle", "sophisticated", "flowing"],
    "tense": ["dramatic", "dark", "tense", "aggressive"],
    "uplifting": ["uplifting", "anthemic", "gospel", "driving", "bright"],
    "dark": ["dark", "minor", "dramatic", "intense", "aggressive"],
}


def get_progressions_by_mood(mood: str) -> list[dict]:
    mood = mood.lower().strip()
    target_tags = MOOD_TAG_MAP.get(mood, [mood])
    results = []
    for p in PROGRESSIONS:
        tags = p.get("tags", [])
        desc = p.get("description", "").lower()
        feel = p.get("feel", "").lower()
        combined = " ".join(tags) + " " + desc + " " + feel
        if any(t in combined for t in target_tags):
            results.append(p)
    return results
